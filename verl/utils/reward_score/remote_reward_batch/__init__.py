def save_to_jsonl(prompt, response):
    record = {
        "prompt": prompt,
        "response": response
    }
    print(record)
    with open(SAVE_JUDGE_PATH, 'a', encoding='utf-8') as f:
        f.write(json.dumps(record, ensure_ascii=False) + '\n')

def match_answer_content(processed_str, answer_pattern = r"(?i)Answer\s*:\s*([^\n]+)"):
    matches = list(re.finditer(answer_pattern, processed_str, re.DOTALL))
    if not matches:
        # print("verify <answer> not matches, return None")
        # print("[Error] No valid answer tags found")
        return None
    final_answer = matches[-1].group(1).strip()
    # print("verify <answer> matches, return final_answer")
    # print(final_answer)
    return final_answer


