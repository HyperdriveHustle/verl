def compute_reward(response):
    reward_score = 0.0
    try:
        boxed_result = last_boxed_only_string(response)
        if boxed_result is not None:
            result = remove_boxed(boxed_result)
            reward_score = float(result == "True")
    except Exception as e:
        print(e)
    return reward_score

def compute_reward_result_tag(judge_response):
    reward_score = 0.0
    acc = 0
    result_match = re.findall(r"<result>(.*?)</result>", judge_response)
    if result_match and len(result_match) >= 1:
        try:
            judge_item_res = True if result_match[-1].strip().lower() == "consistent" else False
            acc = 1 if judge_item_res else -1
        except Exception as e:
            print("match error")
            acc = 0
    return acc


def compute_score(data_source, solution_str, ground_truth, extra_info):

    # problem = extra_info["question"]
    problem = extra_info
    response_str = solution_str[-300:]  # The longest answer in MATH-500 has 159 characters
    # 按照 pattern 抽取出答案的部分
    pred = match_answer_content(
        response_str,
        # dapo prompt
        answer_pattern=r"(?i)Answer\s*:\s*([^\n]+)"
        # answer_pattern = r"\\boxed\s*{([^}]*)}"
    )
    # 根据 box 里面的内容判断（如果有 boxed 的话）
    format_correct = -1.0
    answer_correct = -1.0
    if pred is not None:
        format_correct = 1.0
    response = get_response_by_generate(problem, pred, ground_truth)
    if response is not None:
        answer_correct = compute_reward_result_tag(response)
    else:
        answer_correct = 0.0

    if format_correct < 0:
        reward = format_correct
    else:
        reward = answer_correct

    correct = 0 if reward <= 0 else reward
    acc = correct
    # 和dapo奖励一致 格式错-1，格式对答案错-1，格式对答案对1
    return {
        "score": reward,
        "acc": answer_correct,
        "pred": "" if pred is None else pred,
        # "answer_correct": answer_correct,
    }

